public class ShiftOperator{
public static void main(String[] args) {
int a=2; //10
int i; 
i=a>>1; //01
System.out.println("the value of a before right shfit is: " +a);
System.out.println("the value of a after applying right shfit is: " +i);

int x=2; //10
int k;
k=x<<1; //100
System.out.println("the value of a before left shift is: " +x);
System.out.println("the value of a after applying left shift is: " +k);
	}

}